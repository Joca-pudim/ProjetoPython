# 13. Receba a quantidade de alimento em quilos. Calcule e mostre quantos dias durará esse alimento sabendo que a pessoa consome 50g ao dia.

quantidade_kg = float(input("Digite a quantidade de alimento em kg: "))

quantidade_g = quantidade_kg * 1000
dias = quantidade_g / 50

print("O alimento durará", dias, "dias")

